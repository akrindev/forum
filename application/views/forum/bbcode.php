			Bagaimana membuat teks tebal, huruf miring, dan garis bawah?<br/>
			Untuk membuat suatu bagian teks menjadi tebal,di apit dengan [b][/b] contohnya :
			
			<div class="alert alert-secondary">[b]Halo.[/b]</div>
			
			akan menjadi <br/>
			<div class="alert alert-secondary"><b>Halo.</b></div>
			Untuk memberikan garis bawah, gunakan [u][/u], sebagai contoh:
			
			<div class="alert alert-secondary">[u]Selamat Pagi[/u]</div>
			
			akan menjadi <br/>
			<div class="alert alert-secondary"><u>Selamat Pagi</u></div>
			
			Untuk membuat huruf miring (italic), gunakan [i][/i], contohnya
			<div class="alert alert-secondary">
			Ini [i]Luar Biasa![/i]
			</div>
			akan menghasilkan <br/>
			<div class="alert alert-secondary"><i>Ini Luar Biasa!</i></div>

